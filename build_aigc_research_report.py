def find_file(folder: Path, include: str, suffix: str = "") -> Path:
    for path in folder.glob(f"*{suffix}"):
        if include in path.name:
            return path
    raise FileNotFoundError(include)


def pct(n: float, denom: int) -> str:
    return f"{n / denom * 100:.1f}%"


def clean_label(col: str) -> str:
    text = str(col).strip()
    m = re.search(r"\(([^()]*)\)\s*$", text)
    if m:
        return m.group(1).strip()
    text = re.sub(r"^\d+\s*[、(]?", "", text)
    return text.strip(" )")


def multi_counts(df: pd.DataFrame, cols: list[str]) -> list[dict[str, object]]:
    denom = len(df)
    rows: list[dict[str, object]] = []
    for col in cols:
        s = pd.to_numeric(df[col], errors="coerce").fillna(0)
        n = int((s > 0).sum())
        rows.append({"选项": clean_label(col), "人数": n, "比例": pct(n, denom)})
    return sorted(rows, key=lambda r: (-int(r["人数"]), str(r["选项"])))


def value_counts(df: pd.DataFrame, col: str) -> list[dict[str, object]]:
    denom = len(df)
    vc = df[col].fillna("未填").astype(str).value_counts(dropna=False)
    return [{"选项": k, "人数": int(v), "比例": pct(int(v), denom)} for k, v in vc.items()]


def row_value(df: pd.DataFrame, col_contains: str) -> str:
    return next(c for c in df.columns if col_contains in str(c))


def text_join(rows: list[dict[str, object]], top: int | None = None) -> str:
    use = rows if top is None else rows[:top]
    return "、".join(f"{r['选项']}（{r['比例']}）" for r in use)


def categorize_major(value: object) -> str:
    s = str(value)
    art_keys = ["视觉", "环境", "设计", "艺术", "动画", "摄影", "美术", "广告", "数字媒体", "服装", "产品", "绘画"]
    finance_keys = ["会计", "审计", "财务", "金融", "税收", "工商", "经济", "保险", "国贸", "管理"]
    education_keys = ["教育", "学前", "英语", "传播", "汉语", "新闻", "人文"]
    data_keys = ["计算机", "数据", "人工智能", "信息", "软件"]
    if any(k in s for k in art_keys):
        return "艺术设计类"
    if any(k in s for k in finance_keys):
        return "财经管理类"
    if any(k in s for k in education_keys):
        return "教育人文传播类"
    if any(k in s for k in data_keys):
        return "数据与信息技术类"
    return "其他专业"


def set_cell_text(cell, text: str, bold: bool = False) -> None:
    cell.text = ""
    p = cell.paragraphs[0]
    r = p.add_run(str(text))
    r.bold = bold
    for paragraph in cell.paragraphs:
        for run in paragraph.runs:
            run.font.name = "宋体"
            run._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
            run.font.size = Pt(9)
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER


def set_doc_styles(doc: Document) -> None:
    styles = doc.styles
    normal = styles["Normal"]
    normal.font.name = "宋体"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    normal.font.size = Pt(11)
    normal.paragraph_format.first_line_indent = Pt(22)
    normal.paragraph_format.line_spacing = 1.35
    for name in ["Heading 1", "Heading 2", "Heading 3"]:
        st = styles[name]
        st.font.name = "黑体"
        st._element.rPr.rFonts.set(qn("w:eastAsia"), "黑体")
        st.font.bold = True
    styles["Heading 1"].font.size = Pt(16)
    styles["Heading 2"].font.size = Pt(14)
    styles["Heading 3"].font.size = Pt(12)


def add_page_number(section) -> None:
    footer = section.footer
    p = footer.paragraphs[0]
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    fld_char1 = OxmlElement("w:fldChar")
    fld_char1.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = "PAGE"
    fld_char2 = OxmlElement("w:fldChar")
    fld_char2.set(qn("w:fldCharType"), "end")
    run._r.append(fld_char1)
    run._r.append(instr)
    run._r.append(fld_char2)


def add_table(doc: Document, title: str, rows: list[dict[str, object]], columns: list[str], max_rows: int | None = None) -> None:
    p = doc.add_paragraph(title)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in p.runs:
        run.bold = True
    data = rows if max_rows is None else rows[:max_rows]
    table = doc.add_table(rows=len(data) + 1, cols=len(columns))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.style = "Table Grid"
    for j, col in enumerate(columns):
        set_cell_text(table.cell(0, j), col, bold=True)
    for i, row in enumerate(data, 1):
        for j, col in enumerate(columns):
            set_cell_text(table.cell(i, j), row.get(col, ""))


def add_markdownish(doc: Document, text: str) -> None:
    for raw in text.splitlines():
        line = raw.strip()
        if not line:
            continue
        if re.match(r"^第[一二三四五六七八九十]+章", line):
            doc.add_heading(line, level=1)
        elif re.match(r"^\d+\.\d+\s", line):
            doc.add_heading(line, level=2)
        elif re.match(r"^\d+\.\d+\.\d+\s", line):
            doc.add_heading(line, level=3)
        elif line in {"摘要", "关键词", "参考文献"}:
            doc.add_heading(line, level=1)
        else:
            doc.add_paragraph(line)


